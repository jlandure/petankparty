package org.petank.gwt.client.model;

public class ListePage {

	private String name;
	private String uri;
	
	public ListePage(String name, String uri) {
		this.name = name;
		this.uri = uri;
	}
	
	public String getName() {
		return name;
	}
	public String getUri() {
		return uri;
	}

	
}
